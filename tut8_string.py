myStr = "Saniyaj is a good boy"
print(myStr)
print(myStr[3])  #print the 4th string, beacause index start from 0
print(myStr[0:5])  #print 0 to 4th string. 5 is excluding
print(len(myStr))  #print the length of the string
print(myStr[0:21])  #print the full sting of length 20
#slicing
print(myStr[0:6:2])  #print bouncing 2 steps 0,2,4,6
print(myStr[-4:-1])  #print brom backwords
print(myStr[::-1])  #print the reverse form of string
print(myStr.endswith("boy"))  #print true or false if sting ends with boy
print(myStr.capitalize())
print(myStr.lower())
print(myStr.count("a")) #count total number of a present in the string



